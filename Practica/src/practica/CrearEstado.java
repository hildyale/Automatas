    private void agregarTransicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarTransicionActionPerformed
           
           int item = jComboBox1.getSelectedIndex();
           if(anterior!=item){
               x = 5;
               y = 5;
           }else{
               JComboBox comboBox = new javax.swing.JComboBox<>();
           comboBox.setModel(new javax.swing.DefaultComboBoxModel<>(estados));
           estadoSimbolo[item].add(comboBox);
           comboBox.setBounds(x, y, 56, 20);
           jPanel1.add(comboBox);
           comboBox.setVisible(true);
           jPanel1.repaint();
           contField.setText(estadoSimbolo[item].size()+"");
            if(y>=45){
            x=x+60;
            y=5;
            }else{
            y=y+20;
            }
           }
           anterior = item;
           
           
               
    }//GEN-LAST:event_agregarTransicionActionPerformed

