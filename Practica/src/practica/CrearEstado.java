    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
           int item = jComboBox1.getSelectedIndex();
           if(anterior!=item){
                x = 5;
                y = 5;
                jPanel1.removeAll();
                jPanel1.repaint();
                ArrayList array = estadoSimbolo[item];
                int a = array.size();
                contField.setText(a+"");
                array.forEach((combo) -> {
                    JComboBox comboBox = (JComboBox)combo;
                    comboBox.setBounds(x, y, 56, 20);
                    jPanel1.add(comboBox);
                    comboBox.setVisible(true);
                    if(y>=45){
                        x=x+60;
                        y=5;
                    }else{
                        y=y+20;
                    }
                 });
           }
           anterior = item;
    }//GEN-LAST:event_jComboBox1ActionPerformed

